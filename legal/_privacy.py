"""Политика конфиденциальности."""
from legal._common import COMPANY, render_requisites


def render_privacy_page():
    import streamlit as st

    st.markdown('<div class="sinlex-page-top"></div>', unsafe_allow_html=True)
    st.title("Политика конфиденциальности")
    st.caption(f"Редакция от {COMPANY['updated']}")

    st.markdown(
        f"""
Настоящая Политика конфиденциальности (далее — **Политика**) определяет порядок обработки
и защиты персональных данных пользователей сервиса **{COMPANY["service"]}**
(далее — **Сервис**), предоставляемого **{COMPANY["name"]}** (далее — **Оператор**).

Политика разработана в соответствии с Федеральным законом от 27.07.2006 № 152-ФЗ
«О персональных данных».
        """
    )

    st.markdown("### 1. Оператор персональных данных")
    render_requisites()
    st.markdown(f"Контакт по вопросам персональных данных: **{COMPANY['email']}**")

    st.markdown("### 2. Какие данные мы обрабатываем")
    st.markdown(
        """
- **Учётные данные** (e-mail, название компании, хэш пароля) — регистрация, авторизация, разграничение доступа.
- **Технические данные** (IP-адрес, cookies, идентификатор сессии) — безопасность, поддержание входа.
- **Загружаемые файлы** (STEP, PDF) — анализ геометрии, расчёт.
- **Данные использования** (действия в интерфейсе, параметры проектов) — улучшение Сервиса, поддержка.

Оператор **не запрашивает** специальные категории персональных данных (здоровье, биометрия и т.п.)
и не предназначен для обработки данных несовершеннолетних без согласия законных представителей.
        """
    )

    st.markdown("### 3. Цели и правовые основания")
    st.markdown(
        """
**3.1.** Обработка осуществляется в целях:

- предоставления доступа к Сервису;
- выполнения договора с пользователем / оказания услуг;
- обеспечения информационной безопасности;
- исполнения требований законодательства РФ.

**3.2.** Правовые основания: согласие субъекта, исполнение договора, законный интерес Оператора
при соблюдении прав субъектов, а также иные основания, предусмотренные 152-ФЗ.
        """
    )

    st.markdown("### 4. Cookies и сессии")
    st.markdown(
        """
**4.1.** Сервис использует технические cookies и идентификаторы сессии (`sid`) для поддержания
авторизации и работы личного кабинета.

**4.2.** Пользователь может ограничить cookies в настройках браузера; это может повлиять
на доступность отдельных функций Сервиса.
        """
    )

    st.markdown("### 5. Хранение и защита")
    st.markdown(
        """
**5.1.** Данные хранятся на серверах, используемых для размещения Сервиса, с ограничением доступа
уполномоченных лиц.

**5.2.** Применяются организационные и технические меры: разграничение доступа, аутентификация,
резервное копирование (при наличии), журналирование инцидентов.

**5.3.** Загруженные CAD-файлы и проекты хранятся в рамках учётной записи пользователя / компании
до удаления пользователем или истечения срока хранения, установленного Оператором.
        """
    )

    st.markdown("### 6. Передача третьим лицам")
    st.markdown(
        """
**6.1.** Оператор не продаёт персональные данные.

**6.2.** Передача возможна:

- подрядчикам, обеспечивающим хостинг и инфраструктуру (при наличии договоров о конфиденциальности);
- по законному требованию государственных органов;
- с согласия субъекта.

**6.3.** При использовании внешних API (например, сервисов ИИ-анализа) передаются только данные,
необходимые для запроса, в объёме, согласованном функционалом Сервиса.
        """
    )

    st.markdown("### 7. Права субъекта персональных данных")
    st.markdown(
        f"""
Вы вправе:

- получать сведения об обработке ваших данных;
- требовать уточнения, блокирования или удаления данных при наличии оснований;
- отозвать согласие (если обработка основана на согласии);
- обжаловать действия Оператора в **Роскомнадзор** или в суд.

Запрос направляется на **{COMPANY['email']}** с указанием ФИО и контактов для ответа.
Оператор рассматривает обращения в сроки, установленные 152-ФЗ.
        """
    )

    st.markdown("### 8. Сроки обработки")
    st.markdown(
        """
**8.1.** Персональные данные обрабатываются в течение срока использования Сервиса и до достижения
целей обработки, если более длительный срок не требуется законом.

**8.2.** По истечении сроков данные удаляются или обезличиваются, если иное не предусмотрено
законодательством о хранении документов.
        """
    )

    st.markdown("### 9. Изменение Политики")
    st.markdown(
        """
**9.1.** Оператор вправе обновлять Политику. Актуальная версия доступна в Сервисе.

**9.2.** Существенные изменения могут дополнительно доводиться до сведения пользователей
через интерфейс Сервиса или по e-mail.
        """
    )
